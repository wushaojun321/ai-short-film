# =============================================================================
# 用途：AI 短剧制作 Agent 的基础系统提示词
# 使用场景：用户在 Studio 中与 Agent 对话，通过自然语言修改资产/分镜/视频的提示词
#           并触发重新生成（异步，前端自动轮询刷新）
# 注意：build_system_prompt() 还会动态拼接全剧风格设定和当前对象快照，
